package Loops;

public class WhilePractice2 {
    public static void main(String[] args) {
        /*
        from the age of 18 to 25(inclusive)
        "my age is ..."
         */

        int age=18;
        while(age<=25){
            System.out.println("My age is "+age++);

        }
/*
print out the numbers between 32 to 45
 */

        int number=30;
        while(number<=45){
            System.out.println(number);
            number++;}
        }
    }

