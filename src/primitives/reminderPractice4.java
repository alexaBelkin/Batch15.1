package primitives;

public class reminderPractice4 {
    public static void main(String[] args) {
        int cars=2;
        System.out.println("number of cars after first purchase is "+cars);

        cars=cars+1;
        System.out.println(cars);

        cars+=1;
        System.out.println("number of cars after second purchase is "+ cars);

        cars-=1;
        System.out.println(cars);

        // sharing cars with another person

        cars/=2;
        System.out.println(cars);


        int count=25;
        count%=7;
        System.out.println(count);





    }
}
